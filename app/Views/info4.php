               <p style="text-align: justify;"><strong>KaliputihNews</strong> - Balaidesa Kaliputih melaksanakan kegiatan vaksinasi bagi warga di Desa Kaliputih Kecamatan Singorojo, Kabupaten Kendal. Rabu (15/04/23).

Vaksinasi yang diikuti sebanyak 686 orang, hal ini sesuai dengan data warga yang di vaksin pada dosis 2 sebanyak 686 orang. Dalam kegiatannya kepala desa  mengatakan, “pemberian vaksinasi ini merupakan upaya pemerintah untuk membantu warga dalam memutus penyebaran Covid-19 dan percepatan program imunitas masyarakat. “

Kegiatan Vaksinasi Covid 19 yang di gelar di Desa Kaliputih itu, dilakukan dengan beberapa tahapan mulai dari pengisian blangko, pemeriksaan data, tensi, entri data, pemeriksaan singkat dan jika dinyatakan lolos baru bisa mendapatkan vaksin Covid 19″ imbuh kepala desa.

Secara terpisah kepala desa menambahkan, “Setelah mendapatkan suntikan vaksin, kami menghimbau kepada mereka untuk tetap menjalankan Protokol Kesehatan dengan baik dan benar dan tetap melaksanakan pola hidup sehat.” Katanya.

“Usai mendapatkan suntikan vaksinasi, mereka menjalani observasi selama 30 menit sebelum diperbolehkan pulang dan meninggalkan tempat kegiatan.” tutup kepala desa.
