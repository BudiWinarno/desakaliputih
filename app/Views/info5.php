               <p style="text-align: justify;"><strong>KaliputihNews</strong> – Wakil Bupati Kendal, Windu Suko Basuki menghadiri acara Tasyakuran dalam rangka “Merti Dusun” di Dusun Pencar, Desa Kaliputih, Kecamatan Singorojo, Kamis malam (9/5/2023).

Acara dihadiri Camat Singorojo Sunarto beserta jajaran Forkopimcam Singorojo, Kepala Desa Kaliputih Kusno dan ratusan masyarakat Dusun Pencar dan sekitarnya.

Tak ketinggalan, para pedagang dan penyedia berbagai permainan anak-anak dadakan, juga ikut meramaikan acara.

Pada kesempatan tersebut, Wabup yang akrab disapa Pakde Bas nonton bareng pagelaran wayang kulit, yang menghadirkan Ki Dalang, Tri Agus Setiawan Gondo dari Desa Ngareanak Kecamatan Singorojo dengan lakon “Wahyu Sandang Pangan”.

Dalam sambutannya, Pakde Bas mengapresiasi warga Desa Kaliputih yang masih menjaga budaya Merti dalam rangka mewujudkan rasa syukur atas kemakmuran dusun maupun desa.
                    <br>
                    “Atas nama pribadi dan Pemerintah Kabupaten Kendal, saya mengepresiasi warga Desa Kaliputih yang nguri-uri kabudayan Merti Dusun, dan dengan menampilkan kesenian wayang kulit. Semoga dengan merti dusun ini, Dusun Pencar tambah maju dan masyarakatnya sejahhtera,” ungkapnya.

Pakde Bas juga berpesan, selain melestarikan budaya, acara Merti Dusun juga menjadi ajang konsolidasi bagi masyarakat untuk mewujudkan kebersamaan dalam rangka pencapaian kesejahteraan masyarakat.

“Saya minta semangat gotong royong juga terus dilestarikan dan dijaga oleh warga Dusun Pencar Desa Kaliputih. Hal ini demi untuk kemajuan Desa Kaliputih umumnya dan Dusun Pencar khususnya,” pesannya.

Sementara itu Camat Singorojo, Sunarto dalam sambutannya menjelaskan, pagelaran wayang rutin diadakan dua tahun sekali oleh warga Dusun Pencar Desa Kaliputih.

“Sebagai bentuk upaya melestarikan kearifan lokal yang berbentuk adat tradisi, masyarakat Desa Kaliputih setiap dua tahun sekali melaksanakan kegiatan upacara adat Merti Dusun,” ujar Sunarto.

Kepala Desa Kaliputih, Kusno menambahkan, acara Merti ini sudah menjadi tradisi yang ada di Dusun Pencar, sebagai bentuk syukur kepada Tuhan Yang Maha Esa dengan cara merawat dan menjaga apa saja yang ada di lingkungan dusun.

Dirinya juga mengucapkan terima kasih atas kehadiran Wakil Bupati Kendal dalam acara Merti Dusun di desanya.

“Terima kasih kepada Bapak Wabup yang telah menghadiri acara Merti Dusun di desa kami. Semoga dengan kegiatan Merti Dusun ini bisa bermanfaat bagi masyarakat,” ujarnya. (HS-06).
    
