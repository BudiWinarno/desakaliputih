               <p style="text-align: justify;"><strong>KaliputihNews</strong> - Sekitar 125 ribu lebih warga di Kaliputih terdaftar sebagai keluarga penerima manfaat (KPM) bantuan sosial (bansos) sembako maupun program keluarga harapan (PKH) dari Kementerian Sosial RI. Bansos tersebut mulai dibagikan sejak 29 Maret 2023.
Hingga saat ini, lebih dari 200 warga telah menerima bansos sembako dan PKH yang disalurkan oleh PT Pos Indonesia lewat Kantor Pos Kecamatan Singorojo. Bupati Kendal Dico Ganinduto meninjau penyaluran bantuan tersebut di Kantor Desa Kaliputih, Kecamatan Singorojo, Selasa (4/4/2023).

Kepada penerima, Dico berpesan agar bantuan tersebut dimanfaatkan secara tepat untuk mencukupi kebutuhan sehari-hari. "Gunakan bantuan ini untuk membeli sembako. Jangan sampai uangnya habis buat membeli barang yang sifatnya konsumtif. Namanya saja bansos sembako," pesan Dico.

Kepala Dinas Sosial Pemberdayaan Perempuan, Anak dan Keluarga Berencana, Sri Herliani menjelaskan, bahwa ini adalah penyaluran bansos termin pertama di tahun 2023. Para penerima bansos sembako, akan menerima uang tunai sebesar Rp. 600 ribu.

"Ini merupakan jatah untuk tiga bulan (Januari-Maret 2023) dengan rincian per bulan Rp. 200 ribu. Jadi KPM mendapatkan Rp 600 ribu," urai Sri.
                    <br>
                    Sementara nominal untuk bantuan PKH bervariasi antara Rp 225 ribu hingga Rp. 3 juta-an.
"Nominal PKH yang bervariasi ini dikarenakan data KPM yang berbeda-beda. Misalnya KPM yang memiliki anak usia sekolah, balita, ibu hamil, lansia, dan anggota keluarga yang lumpuh atau cacat," ujar Sri.

Sementara itu, Kepala Kantor Pos Singorojo Trian Nugroho menjelaskan, bahwa untuk saat ini total penerima bansos sembako dan PKH di Kaliputih mencapai 300 KPM. Hingga 3 April 2023, masyarakat yang sudah menerima ada 200 KPM atau 57,15 persen. "Sisanya, kita targetkan sebelum lebaran harus tuntas," ujarnya.

Trian menambahkan, untuk mendekatkan layanan kepada masyarakat, penyaluran dilakukan berbasis dusun. "Tim kami yang hadir ke dusun-dusun, sehingga warga yang rumahnya di pelosok tidak perlu repot datang ke Kantor Pos," ujar Trian.

Dijelaskannya, syarat untuk mengambil bansos ini cukup membawa KTP atau kartu keluarga (KK). Masyarakat yang menerima, adalah mereka yang sudah terdaftar di Data Terpadu Kesejahteraan Sosial (DTKS) Kementerian Sosial.

"Masyarakat bisa hadir di balai desa masing-masing sesuai jadwal. Namun apabila ada yang terlambat mengambil bantuan dari jadwalnya, maka dapat diambil di kantor pos yang ada di Singorojo," ungkapnya.


                 
