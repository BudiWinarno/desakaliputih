               <p style="text-align: justify;"><strong>KaliputihNews</strong> - Tebing setinggi 30 meter di pinggir jalan menuju Desa Kaliputih, Kecamatan Singorojo, Kabupaten Kendal, longsor. Material longsoran menimbun jalan dan sempat mengisolir warga desa.
"Rabu kemarin sore hujan deras di wilayah Singorojo sekitar jam 13.00 WIB dan akibat hujan deras tersebut tebing setinggi 30 meter yang letaknya di pinggir jalan Desa Kaliputih longsor sekitar jam 16.00 WIB dan menutup akses jalan utama. Kalau lokasi longsornya di Dusun Kalidapu dan materialnya menutup badan jalan akses utama, jadi warga Desa Kaliputih ya terisolir," kata Ketua RT 04 Dusun Kalidapu, Desa Kaliputih, Ali, kepada KaliputihNews, Kamis(4/5/2023).

Sejumlah kendaraan roda dua dan empat yang sedang melintas sempat terjebak. Beruntung, tidak ada korban luka atau jiwa dalam kejadian tersebut.

Warga dibantu aparat TNI-Polri dan BPBD Kendal sejak Rabu (3/5) malam berusaha membuka akses jalan utama agar bisa dilalui kendaraan roda dua. Minimnya penerangan membuat evakuasi material longsor dihentikan dan dilanjutkan Kamis (4/5) pagi.

"Kami berusaha menyingkirkan materialnya dulu agar akses jalan bisa dilalui kendaraan roda dua," terangnya.
                    <br>
                    Ali menambahkan, akibat longsor tersebut aktivitas warga menjadi terganggu karena tertutupnya akses jalan utama Desa Kaliputih. Jika ingin ke Boja, warga terpaksa memutar arah atau memilih menerobos akses jalan yang tertimbun longsor untuk keluar dari Desa Kaliputih.

"Kalau mau ke Boja bisa lewat jalan lain tapi harus mutar dulu jauh sekitar 30 kilometer. Tapi kan warga tidak mau ya jadi mereka memilih menerobos jalan yang tertutup longsor," ungkapnya.
                    <br>
                    Menurutnya, di sepanjang jalan Desa Kaliputih, Ali memaparkan ada enam titik yang rawan dengan longsor. Sementara itu salah satu warga Desa Kaliputih, Sugianto mengatakan jalan yang tertimbun longsor kali ini sepanjang sekitar 20 meter.

"Yang tertimbun sekitar 20 meter. Tadi lewat naik motor ya pelan-pelan soalnya licin jadi harus hati-hati," kata Sugianto.
Sugianto berharap Pemkab Kendal segera memberikan bantuan penanganan longsor dan upaya pencegahan dini longsor.

Kapolsek Singorojo, AKP Suharyanto mengatakan TNI-Polri berusaha membantu warga untuk membuka akses jalan utama desa yang tertimbun longsor. Pihaknya juga berkoordinasi dengan BPBD Kendal dan Damkar Kendal untuk melakukan pembersihan agar jalan tidak licin oleh lumpur. Hingga Kamis siang ini warga dan petugas terus berupaya membersihkan material longsor dan membuka kembali akses jalan.

