               <p style="text-align: justify;"><strong>KaliputihNews</strong> - Komunitas Trail Singorojo bersama panitia pembangunan Masjid Al-Muttaqin Dusun Pencar menggelar event trail bakti sosial pembangunan Masjid Al-Muttaqin, Minggu (15/1/2023) bertempat di Dusun Pencar, Desa Kaliputih, Kecamatan Singorojo, Kabupaten Kendal.

Event tersebut diikuti oleh komunitas trail di Kabupaten Kendal, dan komonitas trail di wilayah Jawa Tengah.

Ketua panitia acara dari komunitas trail Singorojo, Abdul Hanif menyampaikan, kegiatan ini dalam rangka Bakti Sosisal penggalangan dana untuk pembangunan Masjid Al-Muttaqin di Dusun Pencar Desa Kaluputih Kecamatan Singorojo.

"Kegiatan ini dilaksankan berkat bekerjasama antara Panitia Masjid dengan komunitas trail Singorojo atau biasa dikenal dengan TSA," ujar Hanif.

                    <br>
                    Hanif juga mengucapkan terima kasih kepada semua pihak yang ikut berpartisipasi dalam kegiatan ini, terutama Pemerintah Kabupaten Kendal.

"Terkait dengan donasi dananya akan disumbangkan kepada Masjid Al-Muttaqin di Dusun Pencar," ungkap Hanif.

Sementara itu, Kasi Trantib Kecamatan Singorojo, Slamet Waryoto menyampaikan, sejak pagi bersama Kapolsek Singorojo sudah hadir memantau pelaksanaan kegiatan, mengingat kegiatan ini sangatlah postif.

Slamet juga mengatakan, kegiatan berjalan dengan baik dan sangat lancar. Hal itu terlihat dari kekompakan para komunitas trail se-Jawa Tengah yang ikut hadir, yang mana sudah sering melakukan bakti sosial.

Salah satu peserta, komunitas trail dari Kaliwungu Selatan, Wahyu menyampaikan, bahwa kegiatan ini sangatlah bagus karena selain menyalurkan hobi juga melakukan bakti sosial untuk pembangunan Masjid.

                   
    
