               <p style="text-align: justify;"><strong>KaliputihNews</strong> - Jembatan Kaliputih, penghubung Dukuh Slento di Desa Kaliputih, Kecamatan Singorojo, Kendal, kembali putus untuk kelima kalinya. Akibatnya, sebanyak 350 warga Dusun Slento terisolir tak bisa pergi ke luar dusun.Putusnya jembatan darurat yang sempat dibangun Maret lalu tersebut terjadi karena terjangan arus banjir di Kaliputih, yang meningkat akibat hujan deras. Kini, untuk menuju dusun lainnya, warga Dusun Slento terpaksa harus menyeberangi sungai yang arusnya cukup deras.

Salah satu warga Dusun Slento, Suroso (50), mengungkapkan, arus banjir yang deras membuat jembatan darurat yang terbuat dari kayu serta bronjong di ujung jembatan, hanyut terbawa kencangnya aliran sungai tersebut.
                    <br>
                    "Beruntung tidak ada korban jiwa karena pada waktu jembatan putus arus sungai deras sekali sehingga warga menjadi urung untuk menyeberang. Ternyata sesuai dugaan, jembatan akhirnya putus kembali karena terbawa arus yang sangat kencang," ujarnya.

Sementara itu, Camat Singorojo, Shobirin, mengatakan, jembatan darurat yang dibangun lebih kurang satu bulan lalu tersebut roboh setelah bronjong penahan batu kali jebol. Kejadian putusnya jembatan sementara ini merupakan yang ke-5 kalinya. Jembatan Kaliputih tersebut pernah putus pada 18 maret 2022, 12 April 2022, 16 Januari 2023, dan 1 maret 2023.

"Jembatan yang putus adalah jembatan darurat yang terbuat dari kayu dan pohon kelapa. Terakhir kali dibuat warga satu bulan lalu atau pas Maret," paparnya.

                    <br>
                    Menurut Shobirin, jembatan berukuran panjang 30 meter dan lebar 2,5 meter tersebut putus pada sisi tengah jembatan. Sebelumnya memang kondisinya telah terlihat miring. Akibat putusnya jembatan tersebut, maka kegiatan warga dari dusun lainnya di Desa Kaliputih menuju Dusun Slento ataupun sebaliknya, menjadi terganggu.

"Rencananya Rabu ini akan diadakan kerja bakti untuk kembali membuat jembatan darurat. Sedangkan dua hari terakhir ini kami masih fokus membuat jalan darurat yang kemarin mengalami longsor," ucapnya.
    
